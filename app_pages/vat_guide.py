def render_wrapped_table(df: pd.DataFrame, col_widths: list[str]) -> None:
    """열 너비를 지정하고 긴 텍스트는 자동 줄바꿈되는 HTML 표를 그립니다."""
    colgroup = "".join(f'<col style="width:{w};">' for w in col_widths)
    thead = "<tr>" + "".join(f"<th>{html_lib.escape(str(c))}</th>" for c in df.columns) + "</tr>"
    body_rows = []
    for _, row in df.iterrows():
        cells = "".join(
            f"<td>{html_lib.escape(str(row[c])).replace(chr(10), '<br>')}</td>" for c in df.columns
        )
        body_rows.append(f"<tr>{cells}</tr>")
    table_html = (
        f'<table class="vat-guide-table"><colgroup>{colgroup}</colgroup>'
        f"<thead>{thead}</thead><tbody>{''.join(body_rows)}</tbody></table>"
    )
    st.markdown(table_html, unsafe_allow_html=True)


